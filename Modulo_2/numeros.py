numero_1 = input('Ingrese su primer numero')
numero_1 = float(numero_1)
numero_2 = input('Ingrese su segundo numero')
numero_2 = float(numero_2)
numero_3 = input('Ingrese su tercer numero')
numero_3 = float(numero_3)


print('resultado de la suma de sus numeros es', numero_1 + numero_2 + numero_3)
print('resultado de la suma de sus numeros es', numero_1 - numero_2 - numero_3)
print('resultado de la suma de sus numeros es', numero_1 * numero_2 * numero_3)
print('resultado de la suma de sus numeros es', numero_1 / numero_2 / numero_3)