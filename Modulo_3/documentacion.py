def suma(sumando1, sumando2):
    '''
    Suma dos numeros

    >>> suma(1, 2)
    3
    >>> suma(5, 10)
    15
    >>> suma(1, 3)
    4

    :param sumando1: num El primer número a evaluar
    :param sumando2: num El número a sumar
    :return: num el resultado de la suma
    '''
    return sumando1 + sumando2